  <div class="wp-block-column" style="flex-basis:66.6%"><!-- wp:paragraph {"fontSize":"x-small"} -->
    <p class="has-x-small-font-size">Senator Steven Roberts was sworn into office on Jan. 6, 2021, to serve the people
      of the 5th Senatorial District and selected as the Democratic Whip for the Minority Caucus. He previously served
      two terms in the Missouri House of Representatives, from 2017 to 2020. While in the House, he was selected by his
      colleagues to serve as Chairman of the Missouri Legislative Black Caucus.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph {"fontSize":"x-small"} -->
    <p class="has-x-small-font-size">Senator Roberts is a fourth-generation St. Louis native, whose parents and
      grandparents overcame segregation and discrimination to establish their careers. His mother, Dr. Eva Frazer,
      became a distinguished doctor and his father a respected attorney, St. Louis alderman and successful businessman.
      It is from his parents that he developed a strong sense of civic responsibility and social justice.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph {"fontSize":"x-small"} -->
    <p class="has-x-small-font-size">Senator Roberts holds a Bachelor of Science in Psychology and Communication Studies
      from the University of Miami and received his law degree from Pepperdine University School of Law. While in law
      school, his experiences included working in the U.S. Congress as a legislative intern; as a certified law clerk
      with the District Attorney's Office in the City of Compton, California; assisting the Counsel to Secure Justice in
      New Delhi, India, to help victims of violent sexual assault; and in his final semester, he worked with a clinic in
      downtown Los Angeles helping refugees fleeing persecution in their country obtain asylum in the United States.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph {"fontSize":"x-small"} -->
    <p class="has-x-small-font-size">After graduating law school, he joined AP Wireless Infrastructure Partners, LLC as
      a member of a team of attorneys responsible for managing over $200 million of company assets. Following the events
      in Ferguson, Steve Roberts returned to serve his community as a prosecutor for the City of St. Louis where he
      participated in over a hundred cases from minor weapons offenses to homicides.</p>
